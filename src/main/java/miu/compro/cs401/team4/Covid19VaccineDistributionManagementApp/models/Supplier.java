package miu.compro.cs401.team4.Covid19VaccineDistributionManagementApp.models;

public class Supplier {
	private int id;
	private String name;
	private String city;
	private String phoneNumber;

	// START Getters
	public int getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public String getCity() {
		return city;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	// END Getters
}
